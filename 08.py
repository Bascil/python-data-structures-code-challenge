# Minimum and maximum

# Minimum - Find the minimum item in a sequence lexicographically 
# but do not mix types

# String
x = 'bug'
print(min(x))

# List
y = ['pig', 'cow', 'horse']
print(min(y))

# Maximum - FInd the maximum item in a sequence
# String
x = 'bug'
print(max(x))

# List
y = ['pig', 'cow', 'horse']
print(max(y))
